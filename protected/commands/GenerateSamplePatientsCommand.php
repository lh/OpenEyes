        $this->saveOrThrow($episode);
        print "Added Episode {$episode->id} for firm {$episode->firm->name}\r\n";

        for ($e = mt_rand(1, $this->event_limit); $e < $this->event_limit; ++$e) {
            $event = $this->generateEvent($episode);
        }

        return $episode;
    }

