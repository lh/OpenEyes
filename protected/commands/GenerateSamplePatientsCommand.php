     * @var array
     */
    private $referenceData = array();

    /**
     * @return string
     */
    public function getHelp()
    {
        return 'Generates a list of sample patients and their related data';
    }

    /**
     * @param string[] $args
     */
    public function run($args)
    {
        if (!empty($args[0])) {
            $this->patient_count = (int)$args[0];
        }

        $this->makeReferenceData();

        /* @var CDbTransaction $transaction */
        $transaction = Yii::app()->db->beginTransaction();
        for ($p = 0; $p < $this->patient_count; ++$p) {
            $patient = $this->generatePatient();
        }

        $transaction->commit();
    }

    /**
     *
     */
    private function makeReferenceData()
    {
        $this->referenceData['EthnicGroup'] = EthnicGroup::model()->findAll();
        $this->referenceData['Country'] = Country::model()->findAll();
        $this->referenceData['AddressType'] = AddressType::model()->findAll();
        $this->referenceData['Firm'] = Firm::model()->findAll();
        $this->referenceData['EpisodeStatus'] = EpisodeStatus::model()->findAll();
        $this->referenceData['EventType'] = EventType::model()->findAll();
        $this->referenceData['first_name'] = $this->getFirstNames();
        $this->referenceData['last_name'] = $this->getLastNames();
        $this->referenceData['address1'] = $this->getAddressOnes();
        $this->referenceData['address2'] = $this->getAddressTwos();
        $this->referenceData['county'] = $this->getCounties();
        $this->referenceData['city'] = $this->getCities();
        $this->referenceData['title'] = $this->getTitles();
    }

    /**
     * @return Patient
     */
    private function generatePatient()
    {
        $patient = new Patient();
        $patient->contact_id = $this->generateContact()->id;

        // unique hos_num
        while (true) {
            $patient->hos_num = mt_rand(999999, 99999999);
            if (!Patient::model()->exists('hos_num = :hos_num', array('hos_num' => $patient->hos_num))) {
                break;
            }
        }

        $patient->nhs_num = $this->randomNhsNumber();
        $patient->dob = $this->randomDate('1920-01-01');
        $patient->gender = $this->randomGender();
        $patient->date_of_death = $this->randomDecision(0.2) ? $this->randomDate($patient->dob) : null;
        $patient->ethnic_group_id = $this->randomDecision(0.5) ? $this->randomEthnicGroup()->id : null;
        $patient->is_local = 1;

        // GP
        if ($this->randomDecision(0.5)) {
            $patient->gp_id = $this->generateGp()->id;
        }

        // Practice
        if ($this->randomDecision(0.5)) {
            $patient->practice_id = $this->generatePractice()->id;
        }

        $this->saveOrThrow($patient);
        print "Added Patient {$patient->id}: {$patient->contact->first_name} {$patient->contact->last_name}\r\n";

        for ($e = mt_rand(0, $this->episode_limit); $e < $this->episode_limit; ++$e) {
            $episode = $this->generateEpisode($patient);
        }

        return $patient;
    }

    /**
     * @param bool $nick_name
     * @param bool $title
     * @return Contact
     */
    private function generateContact($nick_name = false, $title = false)
    {
        $contact = new Contact();
        $contact->first_name = $this->randomFirstName();
        $contact->last_name = $this->randomLastName();
        $contact->primary_phone = mt_rand(99999999, 999999999);
        $contact->title = $title ? $this->randomTitle() : null;

        $this->saveOrThrow($contact);
        print "Added Contact {$contact->id}: {$contact->first_name} {$contact->last_name}\r\n";

        $this->generateAddress($contact->id);

        return $contact;
    }

    /**
