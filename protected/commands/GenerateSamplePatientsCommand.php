    }

    /**
     * @param $start_date
     * @param null $end_date
     * @return false|string
     */
    private function randomDate($start_date, $end_date = null)
    {
        $min = strtotime($start_date);
        $max = strtotime($end_date ?: date('Y-m-d H:i:s'));

        $val = mt_rand($min, $max);

        return date('Y-m-d H:i:s', $val);
    }

    /**
     * @param $min
     * @param $max
     * @return mixed
     */
    private function randomFloat($min, $max)
    {
        return ($min + lcg_value() * (abs($max - $min)));
    }

    /**
     * @param $weight
     * @return bool
     */
    private function randomDecision($weight)
    {
        return $this->randomFloat(0, 1) < $weight;
    }

